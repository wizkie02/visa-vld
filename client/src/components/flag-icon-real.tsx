const sizeClasses = {
  sm: 'text-sm',
  md: 'text-base',
  lg: 'text-xl'
};

export default function FlagIconReal({ country, size = 'md', className = '' }: FlagIconRealProps) {
  const countryCode = COUNTRY_CODES[country.toLowerCase()];
  const sizeClass = sizeClasses[size];

  if (!countryCode) {
    return <span className={`inline-block ${sizeClass} ${className}`}>üåç</span>;
  }

  return (
    <span
      className={`fi fi-${countryCode} ${sizeClass} ${className}`}
      style={{
        fontSize: size === 'sm' ? '1rem' : size === 'md' ? '1.25rem' : '1.5rem',
        lineHeight: 1,
        backgroundSize: 'contain',
        backgroundPosition: '50%',
        backgroundRepeat: 'no-repeat'
      }}
    />
  );
}